/**
 * Blit a pixel buffer to a window.
 */
void graphics_blit(int win_id, int x, int y, int w, int h,
                   const uint32_t *buffer);

/**
 * Draw text in a window using stb_easy_font.
 * Returns the number of pixels wide the text was.
 */
